// interrupt rates for NTSC/VGA
#define VGA_INTERRUPT_RATE   910
#define NTSC_INTERRUPT_RATE  1817
#define VIDEO_INTERRUPT_RATE (VGA_INTERRUPT_RATE)

// width and height of screen 
#define VGA_SCREEN_WIDTH     120           
#define VGA_SCREEN_HEIGHT    96

#define SCREEN_WIDTH         120           
#define SCREEN_HEIGHT        96

// controls the number of lines to shift the display vertically, later assign to global varible, so caller can control
#define VGA_VERTICAL_OFFSET  32
#define VERTICAL_OFFSET      32


// these indicate the start, end, of the entire video scan and of the active lines, always in terms of real video lines 0
// eg. 0..261 for NTSC, 0..524 for VGA
#define START_VIDEO_SCAN      0
#define START_ACTIVE_SCAN     (VERTICAL_OFFSET)
#define END_ACTIVE_SCAN       (SCREEN_HEIGHT*5 + 5 + VERTICAL_OFFSET + 1) 
#define END_VIDEO_SCAN        524

// simply interogate the video driver's curr_line variable and determine where the scanline is
#define VIDEO_ACTIVE(video_line)          ( ((video_line) >= START_ACTIVE_SCAN && (video_line) <= END_ACTIVE_SCAN) ? 1 : 0)
#define VIDEO_INACTIVE(video_line)        ( ((video_line) < START_ACTIVE_SCAN || (video_line) > END_ACTIVE_SCAN) ? 1 : 0)
#define VIDEO_TOP_OVERSCAN(video_line)    ( ((video_line) >= 0 && (video_line) < START_ACTIVE_SCAN) ? 1 : 0)
#define VIDEO_BOT_OVERSCAN(video_line)    ( ((video_line) > END_ACTIVE_SCAN) ? 1 : 0)

// number of bits each pixel is encoded with
#define VGA_PIXELS_PER_BYTE  4            
#define PIXELS_PER_BYTE      4            

// VGA color channel masks
#define VGA_RMASK           0b00000011
#define VGA_GMASK           0b00001100
#define VGA_BMASK           0b00110000

// VGA basic colors
#define VGA_RGB_BLACK       0b00000000
#define VGA_RGB_RED         0b00000011
#define VGA_RGB_GREEN       0b00001100
#define VGA_RGB_BLUE        0b00110000
#define VGA_RGB_WHITE       0b00111111
#define VGA_RGB_GRAY        0b00101010

// VGA sync signals
#define VGA_HSYNC           0b01000000
#define VGA_VSYNC           0b10000000

// VGA driver states
#define VGA_STATE_TOP_OVERSCAN  0
#define VGA_STATE_ACTIVE_VIDEO  1
#define VGA_STATE_BOT_OVERSCAN  2
#define VGA_STATE_VSYNC         3


